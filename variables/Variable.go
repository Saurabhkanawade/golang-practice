package main

import "fmt"

func main() {

	var username string = "Saurabh Kanawade"
	fmt.Println(username)
	fmt.Printf("The Type of the Variable is : %T \n", username)

	var isLoggedIn bool = true
	fmt.Println(isLoggedIn)
	fmt.Printf("The Type of the Variable is : %T \n", isLoggedIn)

	var numberValue int = 888
	fmt.Println(numberValue)
	fmt.Printf("The Type of the variable is : %T \n", numberValue)

	var totalAmount float32 = 792.42523523
	fmt.Println(totalAmount)
	fmt.Printf("The Type of the varable is : %T \n", totalAmount)

	var number float64 = 42.0009392489238428394
	fmt.Println(number)
	fmt.Printf("The Type of the variable is : %T \n", number)

	var name string
	fmt.Println(name)
	fmt.Printf("The type of the variable is : %T \n", name)

	var intValue int
	fmt.Println(intValue)
	fmt.Printf("The type of the variable is : %T \n ", intValue)

}
